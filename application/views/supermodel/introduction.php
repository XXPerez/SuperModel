            <div id="content-inner-2">
                SuperModel is based on avenirer project and extended to allow more functionallity
                <br />
                <a href="https://github.com/avenirer/CodeIgniter-MY_Model">Avenirer/CodeIgniter-MY_Model</a>
                <br />
                <br />
                It's a simple ORM that can be used for small and large projects, allowing you to write fewer lines of code.
                <br />
                It have a simple and clear code, but also powerfull.
                <br />
                <br />
                Whith a simple declaration inside a model, you can:
                <br/><br/>
                <ul>
                    <li>Get one record by primary key</li>
                    <li>Get one record by another criteria</li>
                    <li>Get multiple records by custom criteria</li>
                    <li>Get data records with relationship data</li>
                    <li>Fill data to insert records</li>
                    <li>Custom validate rules to insert/update</li>
                    <li>Update/Delete records by custom criteria</li>
                    <li>Declare allowed fields to be filled by insert/update</li>
                    <li>Protect fields to be filled by insert/update</li>
                    <li>Observers Pre&Post Insert/Update/Read/Delete</li>
                    <li>Automatic fill of custom created_on_data & modified_on_data fields</li>
                    <li>Automatic full_text detection to create where conditions</li>
                    <li>Changes detection, saving all CRUD actions and "before"/"after" values.</log>
                    <li>Declare custom search&filter fields that can be used from get/post and it's relation with real fields & tables</li>
                    <li>Support for max, min, count, count distinct, sum, and other mysql functions over allowed fields.</li>
                    <li>Fields object, setters & getters</li>

                </ul>
                Supermodel does not replace codeigniter ActiveRecord (DB_query_builder), Supermodel uses Codeigniter ActiveRecord.
